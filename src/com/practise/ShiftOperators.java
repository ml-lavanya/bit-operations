package com.practise;

public class ShiftOperators {
    public static void main(String[] args) {
        System.out.println("********* right shift operator means divide by 2 ********");
        System.out.printf("256 >> 2: %1$d", 256>>2);

        System.out.println("********* left shift operator means multiply by 2 ********");
        System.out.printf("256 << 2: %1$d", 256<<2);
    }
}
